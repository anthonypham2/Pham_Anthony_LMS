    public void displayBooks(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management_system","root","");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from checkout");
            
            while(rs.next()){
                String bookName = rs.getString("bookTitle");
                String bookWriter = rs.getString("barcodeID");
                String dueDates = rs.getString("dueDate");
                
                Object[] obj = {bookName, bookWriter, dueDates};
                table =(DefaultTableModel) bookTable.getModel();
                table.addRow(obj);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
    
